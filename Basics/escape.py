# Escape Sequences Examples

# \" - Print double quote
print("\"Hello\"")

# \' - Print single quote
print('It\'s a beautiful day')

# \n - Print newline
print("Line 1\nLine 2\nLine 3")

# \t - Print tab character
print("Name\tAge\nJohn\t25\nDiana\t28")

# \r - Carriage return
print("Hello\rWorld")

# \\ - Print backslash
print("C:\\Users\\Downloads\\file.txt")