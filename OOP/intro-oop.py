# Object-Oriented Programming (OOP) in Python
# ============================================

# OOP is a programming paradigm that organizes code into objects and classes.
# It helps create modular, reusable, and maintainable code.

# KEY CONCEPTS:
# 1. Class: A blueprint for creating objects with attributes and methods
# 2. Object: An instance of a class with specific values
# 3. Encapsulation: Bundling data and methods together, hiding internal details
# 4. Inheritance: Creating new classes based on existing ones
# 5. Polymorphism: Objects can take multiple forms or methods can have multiple behaviors
# 6. Abstraction: Hiding complex implementation details behind a simple interface

# EXAMPLE STRUCTURE:
# - Define a class with __init__() constructor to initialize attributes
# - Add methods to define behaviors
# - Create instances (objects) from the class
# - Use self to reference instance attributes and methods
# - Use inheritance to extend class functionality
# - Override methods in child classes for polymorphism

# BENEFITS:
# - Code organization and structure
# - Reusability through inheritance
# - Easy maintenance and updates
# - Better code readability
# - Real-world modeling of entities